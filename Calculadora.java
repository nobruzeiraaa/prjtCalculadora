package prjtCalculadora;

public class Calculadora {
	
	double numero1;
	double numero2;
	double resultado;	
	
	public void somar() {
		resultado = numero1 + numero2;
	}
	public void dividir() {
		resultado = numero1 / numero2; 
	}
	public void subtrair() {
		resultado = numero1 - numero2;
	}
	public void multiplicar() {
		resultado = numero1 * numero2;
		
	
		
	}
}
